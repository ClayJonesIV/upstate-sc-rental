def build_record(month_str, month_idx):
    """Build a history record for a given month index (0 = Jan 2023)."""
    record = {
        "month": month_str,
        "fetched_at": f"{month_str}-01T00:00:00+00:00",
        "seeded": True,
        "markets": {},
    }
    for mkt_key, data in SEED.items():
        b1  = data["b1"][month_idx]
        b2  = data["b2"][month_idx]
        b3  = data["b3"][month_idx]
        all_rent = data["all"][month_idx]
        dom = data["dom"][month_idx]
        inv = data["inv"][month_idx]
        record["markets"][mkt_key] = {
            "averageRent":         all_rent,
            "medianRent":          all_rent,
            "averageDaysOnMarket": dom,
            "medianDaysOnMarket":  dom,
            "totalListings":       inv,
            "newListings":         round(inv * 0.18),
            "bedrooms": {
                "1": {"averageRent": b1, "averageDaysOnMarket": dom, "totalListings": round(inv * 0.30)},
                "2": {"averageRent": b2, "averageDaysOnMarket": dom, "totalListings": round(inv * 0.42)},
                "3": {"averageRent": b3, "averageDaysOnMarket": dom, "totalListings": round(inv * 0.22)},
                "4": {"averageRent": round(b3 * 1.24), "averageDaysOnMarket": dom, "totalListings": round(inv * 0.06)},
            },
        }
    return record

def main():
    print("Seeding history.json with 27 months of historical data...")
    history = [build_record(m, i) for i, m in enumerate(SEEDED_MONTHS)]
    HISTORY_FILE.parent.mkdir(parents=True, exist_ok=True)
    HISTORY_FILE.write_text(json.dumps(history, indent=2))
    print(f"✅ Seeded {len(history)} months → {HISTORY_FILE}")
    print("   Run fetch_data.py next to append the current month from RentCast.")

if __name__ == "__main__":
    main()
